# conversion of one data type into the other data type is known as type casting in python

#explicit typecasting 
# this means "we say to python that convert string to integer"
# the below example is of Explicit Typecasting

a="1";  #this is string
b="2";
print(int(a)+int(b));

#Implicit Typecasting
a=9.9; #this is float
b=1;  #this is integer
print(a+b); #result is in float hence it is implicit typecasting

